'''Esse é um script criado para transformar horas em segundos. 
As horas serão representadas pela variável h.
Os segundos serão representados pela variável s.'''

#Exercício 1:
h1=1.5 #O número 1 representa o primeiro exercício da tarefa
s=h1*3600 #Usaremos essa multiplicação pois cada hora equivale a 3600 segundos.
print("1. Uma hora e meia contém",int(s),"segundos.")


